package Switch;

public class Switch {
    public static void main(String[] args) {

        int number = 2;
        switch (number) {
            case 1:
                System.out.println("Better than nothing");
                break;
            case 2:
                System.out.println("Much better");
                break;
            case 3:
                System.out.println("Almost perfect");
                break;
            case 4:
                System.out.println("Perfect");
                break;
            default:
                System.out.println("I am not sure..");

        }
    }
}
